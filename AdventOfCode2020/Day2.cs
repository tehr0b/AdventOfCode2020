        private bool IsValidPartOne(Entry entry) {
            var count = 0;
            for (int i = 0; i < entry.str.Length; i++) {
                if (entry.str[i] == entry.character) {
                    count++;
                }
            }

            return count >= entry.min && count <= entry.max;
        }

        private bool IsValidPartTwo(Entry entry) {
            var first = entry.str[entry.min - 1] == entry.character;
            var second = entry.str[entry.max - 1] == entry.character;

            if (first && second) return false;
            return first || second;
        }

